/** スコアから性格タイプを判定 */
export function calculatePersonality(scores: TraitScores): {
  primaryTrait: TraitKey;
  secondaryTrait: TraitKey;
  allTraits: { key: TraitKey; score: number }[];
} {
  const entries = (Object.keys(scores) as TraitKey[]).map((key) => ({
    key,
    score: scores[key],
  }));
  const sorted = [...entries].sort((a, b) => b.score - a.score);

  return {
    primaryTrait: sorted[0].key,
    secondaryTrait: sorted[1].key,
    allTraits: sorted,
  };
}

/** 全質問の選択からスコアを集計 */
export function computeScores(answers: Record<number, string>): TraitScores {
  const scores: TraitScores = {
    行動力: 0,
    論理的傾向: 0,
    責任感: 0,
    探求心: 0,
    ソーシャル駆動: 0,
  };

  for (const q of questions) {
    const chosenId = answers[q.id];
    if (!chosenId) continue;
    const choice = q.choices.find((c) => c.id === chosenId);
    if (!choice) continue;
    for (const [trait, delta] of Object.entries(choice.effects)) {
      scores[trait as TraitKey] += delta;
    }
  }

  return scores;
}
