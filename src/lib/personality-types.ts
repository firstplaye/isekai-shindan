export function getResult(
  primary: TraitKey,
  secondary: TraitKey,
  scores: Record<TraitKey, number>
): PersonalityResult {
  // 複合タイプ判定：2位が1位の80%以上のスコアならハイブリッド
  const primaryScore = scores[primary];
  const secondaryScore = scores[secondary];
  const ratio = primaryScore > 0 ? secondaryScore / primaryScore : 0;

  if (ratio >= 0.8) {
    const hybridKey = `${primary}_${secondary}`;
    if (HYBRID_RESULTS[hybridKey]) return HYBRID_RESULTS[hybridKey];
    const reverseKey = `${secondary}_${primary}`;
    if (HYBRID_RESULTS[reverseKey]) return HYBRID_RESULTS[reverseKey];
  }

  return RESULTS[primary];
}
